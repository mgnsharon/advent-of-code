use std::{cmp::Ordering, str::FromStr};

#[derive(PartialEq, Copy, Clone)]
enum HandShape {
    Rock = 1,
    Paper = 2,
    Scissors = 3,
}

impl HandShape {
    fn from_outcome(other: &Self, s: &str) -> Result<Self, String> {
        match s {
            "X" => match other {
                HandShape::Rock => Ok(HandShape::Scissors),
                HandShape::Paper => Ok(HandShape::Rock),
                HandShape::Scissors => Ok(HandShape::Paper),
            },
            "Y" => match other {
                HandShape::Rock => Ok(HandShape::Rock),
                HandShape::Paper => Ok(HandShape::Paper),
                HandShape::Scissors => Ok(HandShape::Scissors),
            },
            "Z" => match other {
                HandShape::Rock => Ok(HandShape::Paper),
                HandShape::Paper => Ok(HandShape::Scissors),
                HandShape::Scissors => Ok(HandShape::Rock),
            },
            _ => return Err("Invalid Outcome".to_string()),
        }
    }
}

impl PartialOrd for HandShape {
    fn partial_cmp(&self, other: &Self) -> Option<Ordering> {
        match self {
            HandShape::Rock => match other {
                HandShape::Paper => Some(Ordering::Less),
                HandShape::Rock => Some(Ordering::Equal),
                HandShape::Scissors => Some(Ordering::Greater),
            },
            HandShape::Paper => match other {
                HandShape::Paper => Some(Ordering::Equal),
                HandShape::Rock => Some(Ordering::Greater),
                HandShape::Scissors => Some(Ordering::Less),
            },
            HandShape::Scissors => match other {
                HandShape::Paper => Some(Ordering::Greater),
                HandShape::Rock => Some(Ordering::Less),
                HandShape::Scissors => Some(Ordering::Equal),
            },
        }
    }
}

impl FromStr for HandShape {
    type Err = String;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        match s {
            "A" | "X" => Ok(HandShape::Rock),
            "B" | "Y" => Ok(HandShape::Paper),
            "C" | "Z" => Ok(HandShape::Scissors),
            _ => Err("Invalid hand shape".to_string()),
        }
    }
}
enum RoundOutcome {
    Won = 6,
    Lost = 0,
    Draw = 3,
}

fn calc_round_score(s: &str) -> u32 {
    let hands: Vec<HandShape> = s
        .split(" ")
        .map(|h| h.parse::<HandShape>().unwrap())
        .collect();
    let opp_hand = hands.get(0).unwrap();
    let own_hand = hands.get(1).unwrap();

    match own_hand.partial_cmp(opp_hand).unwrap() {
        Ordering::Equal => RoundOutcome::Draw as u32 + *own_hand as u32,
        Ordering::Greater => RoundOutcome::Won as u32 + *own_hand as u32,
        Ordering::Less => RoundOutcome::Lost as u32 + *own_hand as u32,
    }
}

fn calc_modified_score(s: &str) -> u32 {
    let mut item_iter = s.split(" ").into_iter();
    let opp_hand = HandShape::from_str(item_iter.next().unwrap()).unwrap();
    let own_hand = HandShape::from_outcome(&opp_hand, item_iter.next().unwrap()).unwrap();

    match own_hand.partial_cmp(&opp_hand).unwrap() {
        Ordering::Equal => RoundOutcome::Draw as u32 + own_hand as u32,
        Ordering::Greater => RoundOutcome::Won as u32 + own_hand as u32,
        Ordering::Less => RoundOutcome::Lost as u32 + own_hand as u32,
    }
}

pub fn day_2a(s: &str) -> String {
    let result: u32 = s.lines().map(calc_round_score).sum();
    result.to_string()
}

pub fn day_2b(s: &str) -> String {
    let result: u32 = s.lines().map(calc_modified_score).sum();
    result.to_string()
}

